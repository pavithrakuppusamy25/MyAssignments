package week2.day2;

public class LearnSwitch {

	public static void main(String[] args) {
		// Switch statement
		int a=1;// out of loop
		switch (a) {
		
		case 1:
			System.out.println("Monday");
			
			break;
		case 2:
			System.out.println("Tuesday");
			break;
		case 3:
			System.out.println("wedenesday");
			break;
			
		default:
			System.out.println("Invaild");
			break;
		}
	}

}
