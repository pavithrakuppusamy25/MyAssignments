package week2.day2;

public class LearnJumpStatement {

	public static void main(String[] args) {
		// Break statement in loop
		for (int i = 0; i < 10; i++) {
			if(i==8) {
				
				//break;
				continue; //skip the specific execution
			}
			System.out.println(i);
			
		}

	}

}
